#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int				sign;
	unsigned int	i;
	unsigned int	num;

	i = 0;
	sign = 1;
	num = 0;
	while (nptr[i] == ' ' || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			sign *= -1;
		i++;
	}
	while (nptr[i] && nptr[i] >= '0' && nptr[i] <= '9')
		num = num * 10 + nptr[i++] - '0';
	if (sign == -1 && num > 2147483648)
		return (0);
	if (sign == 1 && num > 2147483647)
		return (-1);
	return (num * sign);
}
